package cn.pc.exam.pojo;

public class Course {
    private String Cid;
    private String Cname;
    private String Cexamtime;

    public String getCid() {
        return Cid;
    }

    public void setCid(String cid) {
        Cid = cid;
    }

    public String getCname() {
        return Cname;
    }

    public void setCname(String cname) {
        Cname = cname;
    }

    public String getCexamtime() {
        return Cexamtime;
    }

    public void setCexamtime(String cexamtime) {
        Cexamtime = cexamtime;
    }
}
