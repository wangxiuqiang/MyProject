package cn.pc.exam.pojo;

public class Exam {
    private String Eid;
    private String Ename;
    private String Echair;
    private String Eexamtime;

    public String getEid() {
        return Eid;
    }

    public void setEid(String eid) {
        Eid = eid;
    }

    public String getEname() {
        return Ename;
    }

    public void setEname(String ename) {
        Ename = ename;
    }

    public String getEchair() {
        return Echair;
    }

    public void setEchair(String echair) {
        Echair = echair;
    }

    public String getEexamtime() {
        return Eexamtime;
    }

    public void setEexamtime(String eexamtime) {
        Eexamtime = eexamtime;
    }
}
